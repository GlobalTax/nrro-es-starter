/** Get a route by path */
export function getRouteByPath(path: string): SiteRoute | undefined {
  const normalized = path === '/' ? '/' : path.replace(/\/+$/, '');
  return siteRoutes.find(r => r.path === normalized);
}

/** Get all routes for a given language */
export function getRoutesByLang(lang: 'es' | 'ca' | 'en'): SiteRoute[] {
  return siteRoutes.filter(r => r.lang === lang);
}
